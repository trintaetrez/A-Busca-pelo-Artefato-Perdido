package entities;

public class Heroi {

	protected Integer life = 100;
	protected Integer Gold = 20;
	
	public Heroi() {
	}

	public Integer getLife() {
		return life;
	}

	public void setLife(Integer life) {
		this.life = life;
	}

	public Integer getGold() {
		return Gold;
	}

	public void setGold(Integer gold) {
		Gold = gold;
	}
	
}
